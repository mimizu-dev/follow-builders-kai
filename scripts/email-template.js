// ---------------------------------------------------------------------------
// Parser — converts plain-text digest into typed block objects
// ---------------------------------------------------------------------------

function parseDigest(text) {
  const lines = text.split('\n');
  const blocks = [];
  let i = 0;
  let afterDivider = false; // true immediately after a ━━━ line

  // Title
  if (lines[0]?.trim().startsWith('AI Builders Digest')) {
    blocks.push({ type: 'title', content: lines[0].trim() });
    i = 1;
  }

  for (; i < lines.length; i++) {
    const line = lines[i].trim();

    if (!line) { afterDivider = false; continue; }

    // ━━━ section divider
    if (/^━+$/.test(line)) { afterDivider = true; continue; }

    // Line that immediately follows ━━━ is a section header
    if (afterDivider) {
      blocks.push({ type: 'section', content: line });
      afterDivider = false;
      continue;
    }

    afterDivider = false;

    if (line === '---') { blocks.push({ type: 'separator' }); continue; }

    if (/^https?:\/\/\S+$/.test(line)) {
      blocks.push({ type: 'url', content: line }); continue;
    }

    // Builder name: **Name**（role）
    if (/^\*\*/.test(line)) {
      blocks.push({ type: 'builder', content: line }); continue;
    }

    blocks.push({ type: 'paragraph', content: line });
  }

  return blocks;
}

// ---------------------------------------------------------------------------
// Renderer — turns blocks into HTML strings
// ---------------------------------------------------------------------------

function renderBlocks(blocks) {
  const parts = [];
  let prevType = null;

  for (let i = 0; i < blocks.length; i++) {
    const b = blocks[i];

    switch (b.type) {
      case 'title':
        // Title is used only for subject; skip in body
        break;

      case 'section': {
        const gap = prevType && prevType !== 'title' ? 'margin-top:40px;' : 'margin-top:28px;';
        parts.push(
          `<div style="${gap}padding-bottom:10px;border-bottom:2px solid ${BRAND};margin-bottom:4px;">` +
          `<p style="margin:0;font-family:-apple-system,BlinkMacSystemFont,'Segoe UI',Helvetica,Arial,sans-serif;` +
          `font-size:10px;font-weight:700;letter-spacing:0.12em;text-transform:uppercase;color:${BRAND};">` +
          `${escHtml(b.content)}</p></div>`
        );
        break;
      }

      case 'builder': {
        // Parse **Name**（role）or **Name** (role)
        const m = b.content.match(/^\*\*(.+?)\*\*\s*(.*)/);
        const name = m ? m[1] : b.content.replace(/\*\*/g, '');
        const role = m ? m[2].replace(/^[（(]|[）)]$/g, '').trim() : '';
        const topGap = (prevType && prevType !== 'section') ? 'margin-top:28px;' : 'margin-top:20px;';
        parts.push(
          `<div style="${topGap}">` +
          `<p style="margin:0 0 1px;font-family:-apple-system,BlinkMacSystemFont,'Segoe UI',Helvetica,Arial,sans-serif;` +
          `font-size:17px;font-weight:700;color:${DARK};letter-spacing:-0.01em;">${escHtml(name)}</p>` +
          (role
            ? `<p style="margin:0 0 10px;font-family:-apple-system,BlinkMacSystemFont,'Segoe UI',Helvetica,Arial,sans-serif;` +
              `font-size:13px;color:${MUTED};">${escHtml(role)}</p>`
            : '') +
          `</div>`
        );
        break;
      }

      case 'paragraph':
        parts.push(
          `<p style="margin:0 0 10px;font-family:-apple-system,BlinkMacSystemFont,'Segoe UI',Helvetica,Arial,sans-serif;` +
          `font-size:15px;line-height:1.75;color:${TEXT};">${renderInline(b.content)}</p>`
        );
        break;

      case 'url': {
        // Group consecutive URLs together
        parts.push(
          `<p style="margin:2px 0;font-family:-apple-system,BlinkMacSystemFont,'Segoe UI',Helvetica,Arial,sans-serif;">` +
          `<a href="${escHtml(b.content)}" style="font-size:13px;color:${BRAND};text-decoration:none;` +
          `font-family:'SFMono-Regular',Consolas,'Liberation Mono',Menlo,monospace;">` +
          `→ ${escHtml(b.content)}</a></p>`
        );
        break;
      }

      case 'separator':
        parts.push(
          `<div style="margin:24px 0;height:1px;background-color:${DIVIDER};"></div>`
        );
        break;
    }

    if (b.type !== 'title') prevType = b.type;
  }

  return parts.join('\n          ');
}

// ---------------------------------------------------------------------------
// Inline markdown renderer — handles **bold**, *italic*
// ---------------------------------------------------------------------------

function renderInline(text) {
  let s = escHtml(text);
  s = s.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');
  s = s.replace(/\*(.+?)\*/g, '<em>$1</em>');
  return s;
}

function escHtml(str) {
  return String(str ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}
